package com.ejisto.event.listener;

import org.junit.Test;

import com.ejisto.event.listener.WebApplicationLoader;

import static junit.framework.Assert.*;

public class WebApplicationLoaderTest {
	
	@Test
	public void testGetContextPath() {
		WebApplicationLoader webApplicationLoader = new WebApplicationLoader();
		assertEquals("/simpleWarProject",webApplicationLoader.getContextPath("/tmp/ejisto/jetty/webapps/simpleWarProject/"));
		assertEquals("/simpleWarProject",webApplicationLoader.getContextPath("c:\\Windows\\Temp\\Space dir\\ejisto\\jetty\\webapps\\simpleWarProject\\"));
		assertEquals("/simpleWarProject",webApplicationLoader.getContextPath("/tmp/ejisto12 3/jetty/webapps/simpleWarProject/"));
		assertEquals("/simpleWarProject",webApplicationLoader.getContextPath("/tmp/(ejisto12 3)/jetty/webapps/simpleWarProject/"));
		assertEquals("/simpleWarProject",webApplicationLoader.getContextPath("/tmp/(ejisto12 3)/jetty/webapps/simpleWarProject"));
	}

}
